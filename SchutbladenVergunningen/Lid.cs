using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SchutbladenVergunningen
{
    public class Lid
    {
        public string Naam { get; set; }
        public string Adres { get; set; }
        public string Ploeg { get; set; }
        public string Gemeente { get; set; }
        public DateTime? Geb { get; set; }
        public string Nat { get; set; }
        public string Gesl { get; set; }
        public string Email { get; set; }
        public string Telefoonnummers { get; set; }
        public string Licentie { get; set; }

        public int? RugNummer { get; set; }
        public string RugNummerTekst
        {
            get
            {
                if (RugNummer.HasValue)
                {
                    return RugNummer.Value.ToString();
                }
                return "";
            }
        }
    }
}
